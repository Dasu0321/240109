# 숫자를 2개 입력받아 큰수와 작은수를 출력하시오
# ex) 5와 8중 큰수는 8, 작은수는 5입니다.
num1=int(input('값1:'))
num2=int(input('값2:'))
num3=max(num1,num2)
num4=min(num1,num2)
print(f'{num1}와 {num2}중 큰 수는 {num3}, 작은수는 {num4}입니다.')
