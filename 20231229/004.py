# 논리연산부정 print(not 명제) -> 참거짓이 반대로 출력됨
print(not True)
print(not False)
print(not 5>3)

# 파이썬에서 논리연산을 사용하면 0은 거짓, 0이 아니면 참
# print(0) -> False 출력, print(not 0) -> True 출력
print(5)
print(not 5)
print(not 0)
print(0==True)
# 파이썬에서 논리연산을 사용하면 ''은 거짓
print(not'')

